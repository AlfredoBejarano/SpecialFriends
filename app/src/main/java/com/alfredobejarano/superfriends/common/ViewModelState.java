package com.alfredobejarano.superfriends.common;

/**
 * Enum class that defines different states in which a ViewModel can be in.
 * @author Alfredo Bejarano
 */
public enum ViewModelState {
    /**
     * This state defines that a ViewModel is performing operations.
     */
    STATE_BUSY,
    /**
     * This state defines that a ViewModel is ready to perform operations.
     */
    STATE_READY
}
